// 흡연 여부 변환
export const convertSmokingToBoolean = (smoking: string): boolean => {
  return smoking === "한다";
};

// 코골이 여부 변환
export const convertSnoringToBoolean = (snoring: string): boolean => {
  return snoring === "네";
};

// 벌레 잡기 여부 변환
export const convertBugCatchingToBoolean = (bugCatching: string): boolean => {
  return bugCatching === "잡는다";
};

